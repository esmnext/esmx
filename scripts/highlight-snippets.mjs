import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

import { codeToHtml } from 'shiki';

/**
 * Pre-highlight each demo's source snippet with Shiki at build/author time and
 * emit a static HTML string the app renders as-is. Shiki (and its grammars)
 * therefore never reach the client bundle — only the resulting HTML string does.
 *
 * Source of truth is each app's `src/snippet.txt` (plain text, no escaping);
 * this writes `src/snippet.generated.ts` next to it. Re-run after editing any
 * snippet.txt: `pnpm highlight:snippets`.
 *
 * Dual-theme output (`defaultColor: false`) emits `--shiki-light` / `--shiki-dark`
 * CSS variables per token; `components.css` picks the active one to match the
 * site's `[data-theme]` + `prefers-color-scheme` scheme.
 */

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const MICRO_APP_DIR = join(ROOT, 'examples/micro-app');

// Demo app directory -> Shiki language of its `src/snippet.txt`. Keep in sync
// with the demos that render a source card (all 15 framework demos).
const APP_LANGUAGES = {
    'ssr-micro-html': 'typescript',
    'ssr-micro-rsbuild-html': 'typescript',
    'ssr-micro-vite-html': 'typescript',
    'ssr-micro-vue2': 'vue',
    'ssr-micro-vue3': 'vue',
    'ssr-micro-rsbuild-vue3': 'vue',
    'ssr-micro-vite-vue3': 'vue',
    'ssr-micro-react': 'tsx',
    'ssr-micro-rsbuild-react': 'tsx',
    'ssr-micro-vite-react': 'tsx',
    'ssr-micro-preact': 'tsx',
    'ssr-micro-preact-htm': 'typescript',
    'ssr-micro-solid': 'tsx',
    'ssr-micro-svelte': 'svelte',
    'ssr-micro-lit': 'typescript'
};

for (const [dir, lang] of Object.entries(APP_LANGUAGES)) {
    const source = join(MICRO_APP_DIR, dir, 'src/snippet.txt');
    const code = readFileSync(source, 'utf8').replace(/\n+$/, '');

    const html = await codeToHtml(code, {
        lang,
        themes: { light: 'github-light', dark: 'github-dark' },
        defaultColor: false
    });

    const target = join(MICRO_APP_DIR, dir, 'src/snippet.generated.ts');
    writeFileSync(
        target,
        '// Generated by scripts/highlight-snippets.mjs from ./snippet.txt — do not edit.\n' +
            '// Re-run `pnpm highlight:snippets` after changing snippet.txt.\n' +
            `export const highlightedSnippet = ${JSON.stringify(html)};\n`
    );
    console.log(`highlighted ${dir}/src/snippet.txt (${lang})`);
}
